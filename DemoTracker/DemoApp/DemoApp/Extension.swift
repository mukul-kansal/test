//MARK: UIColor
extension UIColor {
    var trackerColor:UIColor {
        return UIColor(red: 48/255, green: 51/255, blue: 57/255, alpha: 1.0)
    }
    
    var poweredColor:UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.8)
    }
}
